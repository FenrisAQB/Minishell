#include "../includes/libft.h"

int	ft_atoi(const char *str)
{
	int	nb;
	int	i;
	int	neg;

	nb = 0;
	neg = 1;
	i = 0;
	while (str[i] == 9 || str[i] == 10 || str[i] == 11
		|| str[i] == 12 || str[i] == 13 || str[i] == 32)
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			neg = -1;
		i++;
	}
	while (str[i] > 47 && str[i] < 58)
	{
		nb *= 10;
		nb += str[i] - 48;
		i++;
	}
	return (nb * neg);
}
